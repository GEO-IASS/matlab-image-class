classdef Transform3D < Transform
%TRANSFORM3D  Abstract class for 3D->3D transform
%   
%   Such transforms focus on 3D transforms.
%   Inputs are 3D points, output are 3D points.
%   3D points are represented by a row vector with 3 elements, one element
%   for each coordinate x, y, and z.
%
%   Arrays of planar points are represented by a Nx3 array of double, each
%   column corresponding to the x, y, and z coordinate respectively.
%
%   See also
%   Transform, Transform2D
%
%
% ------
% Author: David Legland
% e-mail: david.legland@grignon.inra.fr
