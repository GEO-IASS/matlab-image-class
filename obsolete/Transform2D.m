classdef Transform2D < Transform
%TRANSFORM2D  Abstract class for 2D->2D transform
%   
%   Such transforms focus on planar transforms.
%   inputs are 2D points, output are 2D points.
%   Planar points are represented by a row vector with 2 elements.
%   Arrays of planar points are represented by a Nx2 array of double,
%   the first column corresponding to the x coordinate, the second column
%   corresponding to the y coordinate.
%
%   See also
%   Transform, Transform3D
%
%
